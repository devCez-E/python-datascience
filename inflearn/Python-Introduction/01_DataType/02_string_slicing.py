# 문자형 - Slicing

str_slicing = "Nice Python"
print(len(str_slicing))
print(str_slicing[0:3])
print(str_slicing[5:])
print(str_slicing[:5])
print(str_slicing[:len(str_slicing) - 3])
print(str_slicing[1:9:2])

print(str_slicing[-6:])
print(str_slicing[1:-2])
print(str_slicing[::2])
print(str_slicing[::-1])


